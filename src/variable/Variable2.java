package variable;

public class Variable2 {
    public static void main(String[] args) {
        // 정수 (기본 - int)
        int a = 10;
        byte a1 = 10;
        byte a2 = 10;
        byte a3 = (byte) (a1 + a2);
        // 실수
        double d = 10;
        float f = 10.0F;
        // boolean
        boolean b = true;
        long a5 = 2150000000L;
        long a6 = 100000000 + 10000L;
        System.out.println(Integer.toBinaryString(44032)); // 44032 - '가'
        char ch2 = '\u0042'; // unicode
    }
}
